# ============================================================
#  ground_truth.py — RAG Evaluation Benchmark Dataset
# ============================================================
#
#  What this file does:
#  Stores hardcoded ground-truth question/answer-location pairs used to
#  measure RAG retrieval quality. Each entry pairs a real question against
#  the exact page number and filename where its answer actually lives in
#  the source PDF — verified by hand against the real documents.
#
#  What this file does NOT handle:
#  Does not call query_rag() or any retrieval logic — pure data only.
#  Does not run evaluation — that belongs to evaluator.py.
#  Does not generate questions automatically — every entry here was written
#  by a human reading the actual PDF, not by the system being evaluated.
#  (Generating ground truth from the same pipeline being tested would make
#  the evaluation circular — it would always "pass" against its own bias.)
#
#  Datasets:
#  - AGI_DATASET                → 01_Artificial_General_Intelligence_Report.pdf
#  - CYBERSECURITY_DATASET      → 02_Cybersecurity_Threat_Intelligence_Report.pdf
#  - QUANTUM_DATASET            → 03_Quantum_Computing_Research_Report.pdf
#  - RENEWABLE_ENERGY_DATASET   → 04_Renewable_Energy_Transition_Report.pdf
#  - CLIMATE_CHANGE_DATASET     → 05_Climate_Change_Mitigation_Report.pdf
#  - ALL_QUESTIONS              → all five datasets combined, for full-corpus
#                                  evaluation across the entire test collection
#
#  Each entry shape:
#  {
#      "question"     : str — natural language question a real user might ask
#      "correct_page" : int — 1-indexed page where the answer actually appears
#      "correct_file" : str — exact filename as stored in ChromaDB metadata
#  }
#
#  Used by:
#  - evaluator.py → iterates every entry, calls query_rag() with the question
#                  and a where={"file_name": correct_file} filter, then checks
#                  whether any of the top-K results match correct_page —
#                  this is exactly the same where-filter mechanism Stage 4's
#                  search_documents() uses in production, so passing here is
#                  real evidence the multi-document isolation actually works.
#
#  Why this dataset must stay human-written:
#  Ground truth is the reference the system is being measured against. If the
#  questions or correct_page/correct_file values were generated by the same
#  RAG pipeline being evaluated, any systematic bias in chunking or retrieval
#  would be baked into both the test and the answer key — recall@K would
#  always look artificially perfect. Every entry below was written by reading
#  the actual source PDF page by page.
# ============================================================


